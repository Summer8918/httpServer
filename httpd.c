int main(void)
{
    int server_sock = -1;
    u_short port = 4000;
    int client_sock = -1;
    struct sockaddr_in client_name;
    socklen_t  client_name_len = sizeof(client_name);
    pthread_t newthread;
    /*初始化 httpd 服务，包括建立套接字，绑定端口，进行监听等*/
    server_sock = startup(&port);
    printf("httpd running on port %d\n", port);

    size_t threads_size=4;
    ThreadPool pool(threads_size);  //创建能并发执行4个线程的线程池

    while (1)
    {
        //accept从已完成连接队列头返回下一个已完成的连接，成功会返回非负描述符
        client_sock = accept(server_sock,
                (struct sockaddr *)&client_name,
                &client_name_len);
        if (client_sock == -1){
            error_die("accept");
        }
        //将任务加入线程池
        pool.enqueue(accept_request,(void*)(intptr_t)client_sock);
    }
    close(server_sock);

    return(0);
}
